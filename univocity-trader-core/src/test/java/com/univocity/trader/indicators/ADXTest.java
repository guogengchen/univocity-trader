    private Candle newCandle(int i) {
        return CandleHelper.newCandle(i, prices[i][0], prices[i][3], prices[i][1], prices[i][2]);
    }

    private void accumulateAndTest(Indicator ind, int expectedIndex, int row) {
        ind.accumulate(newCandle(row));
        double value = ind.getValue();
        double expected = prices[row][expectedIndex];
        assertEquals("Candle " + row, expected, value, 0.01);
    }

    private void testIndicator(Indicator ind, int expectedValueColumnIndex) {
        for (int i = 0; i < prices.length; i++) {
            accumulateAndTest(ind, expectedValueColumnIndex, i);
        }
    }

